<?php

namespace DsBundle\Entity;

/**
 * Usuario
 */
class Usuario
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $nombre;

    /**
     * @var string
     */
    private $apaterno;

    /**
     * @var string
     */
    private $amaterno;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nombre
     *
     * @param string $nombre
     *
     * @return Usuario
     */
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;

        return $this;
    }

    /**
     * Get nombre
     *
     * @return string
     */
    public function getNombre()
    {
        return $this->nombre;
    }

    /**
     * Set apaterno
     *
     * @param string $apaterno
     *
     * @return Usuario
     */
    public function setApaterno($apaterno)
    {
        $this->apaterno = $apaterno;

        return $this;
    }

    /**
     * Get apaterno
     *
     * @return string
     */
    public function getApaterno()
    {
        return $this->apaterno;
    }

    /**
     * Set amaterno
     *
     * @param string $amaterno
     *
     * @return Usuario
     */
    public function setAmaterno($amaterno)
    {
        $this->amaterno = $amaterno;

        return $this;
    }

    /**
     * Get amaterno
     *
     * @return string
     */
    public function getAmaterno()
    {
        return $this->amaterno;
    }
    /**
     * @var \DsBundle\Entity\Domicilio
     */
    private $dom;


    /**
     * Set dom
     *
     * @param \DsBundle\Entity\Domicilio $dom
     *
     * @return Usuario
     */
    public function setDom(\DsBundle\Entity\Domicilio $dom = null)
    {
        $this->dom = $dom;

        return $this;
    }

    /**
     * Get dom
     *
     * @return \DsBundle\Entity\Domicilio
     */
    public function getDom()
    {
        return $this->dom;
    }
}
